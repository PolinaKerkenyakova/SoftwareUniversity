function thePianist(inputArr) {

    let input = [...inputArr];
    let piecesCount = input.shift();
    let piecesNames = input.shift().split('|');

    let piecesArr = [];

    for (let name of piecesNames) {
        let piece = {
            name: name,
        }
        piecesArr.push(piece);
    }

    let command = input.shift();

    while (command !== 'Stop') {



        command = input.shift();
    }
}

